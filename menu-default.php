    {
        //set the button glyphicon to plus or minus based on what was previous
        var span_data = obj[0].innerHTML;
        if(span_data.indexOf('glyphicon-plus') >=0 )
        {
            var span_replace = span_data.replace('glyphicon-plus', 'glyphicon-minus');
        }
        else
        {
            var span_replace = span_data.replace('glyphicon-minus', 'glyphicon-plus');
        }
        $(obj[0]).html(span_replace);
